// the objective is simple here
// take a variable a and check if it is even or odd
// then print the result

class if_else_basic {
    public static void main(String[] args) {
        int a = 100;
        if (a % 2 == 0) {
            System.out.println("even");
        } else {
            System.out.println("odd");
        }
    }
}